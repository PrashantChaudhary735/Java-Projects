public class IfElse {
    static void main(String[] args) {
        int age = 18;
        if (age >= 18) {
            System.out.println("Yes, you can drive your car");
        }
        else if(age < 18)
        {
            System.out.println("You, can not drive the car");
        }
        else {
            System.out.println("Invalid age");
        }


    }
}